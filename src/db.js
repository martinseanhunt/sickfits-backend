// prisma-binding allows us to inverface with prisma in JS
const { Prisma } = require('prisma-binding')

// Connects to remote prisma DB
const db = new Prisma({
  typeDefs: 'src/generated/prisma.graphql',  // This is why we created the post deploy hook, so we can feed this file generated by the prisma server to our application
  endpoint: process.env.PRISMA_ENDPOINT,
  secret: process.env.PRISMA_SECRET,
  debug: process.env.PRISMA_DEBUG
})

module.exports = db